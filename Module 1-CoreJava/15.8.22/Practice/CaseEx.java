package a168;

public class CaseEx
{
	public static void main(String[] args) 
	{
		String s1="shama";
		String s2="RAYANI";
		
		System.out.println(s1.toUpperCase());
		System.out.println(s2.toLowerCase());
		
		System.out.println(s1.startsWith("sh"));
		System.out.println(s1.endsWith("ma"));
		
		System.out.println(s1.charAt(2));
	}
}
